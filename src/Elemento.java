
public class Elemento {
	
	
	String  valor;
	
	Elemento proximo;
	
	
	public Elemento(String valor) {
		
		this.valor = valor;
	}

	public String getValor() {
		return valor;
	}

	public void setValor(String valor) {
		this.valor = valor;
	}

	public Elemento getProximo() {
		return proximo;
	}

	public void setProximo(Elemento proximo) {
		this.proximo = proximo;
	}
	
	
 
}
